ticks = 0
function onTick()
click = input.getBool(4)

output.setBool(5,toggle(click))
end
function toggle(clicked)
if clicked and out and active then
    out = false
    active = false
else
    if clicked and not out and active then 
        out = true
        active = false
    end
end
if not clicked then
    active = true
end
return out
end